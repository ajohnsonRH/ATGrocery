    /**
     * You should remove this class, it only exists as a reference to you.
     * You might also need to run `gradle.bat endpointsSrcClasses` to generate
     * the API code for you.
     */
    static class EndpointsAsyncTask extends AsyncTask<Void, Void, GroceryItemCollection> {
        private static AtgApi myApiService = null;
        private final Context context;

        EndpointsAsyncTask(Context context) {
            this.context = context;
        }

        @Override
        protected GroceryItemCollection doInBackground(Void... params) {
            if (myApiService == null) {  // Only do this once
                AtgApi.Builder builder = new AtgApi.Builder(AndroidHttp.newCompatibleTransport(),
                        new AndroidJsonFactory(), null)
                        // options for running against local devappserver
                        // - 10.0.2.2 is localhost's IP address in Android emulator
                        // - turn off compression when running against local devappserver
                        .setRootUrl("https://atg-api.appspot.com/_ah/api/")
                        .setGoogleClientRequestInitializer(new GoogleClientRequestInitializer() {
                            @Override
                            public void initialize(AbstractGoogleClientRequest<?> abstractGoogleClientRequest) throws IOException {
                                abstractGoogleClientRequest.setDisableGZipContent(false);
                            }
                        });
                // end options for devappserver

                myApiService = builder.build(); // I would make this available via some singleton or something.
            }
            try {
                return myApiService.getAllGroceries().execute();
            } catch (IOException e) {
                Log.e("ATG", e.getMessage());
                return null;
            }
        }

        @Override
        protected void onPostExecute(GroceryItemCollection result) {
            if (result == null) {
                Log.d("ATG", "No groceries");
            }
            for (GroceryItem g : result.getItems()) {
                Log.d("ATG", g.getName());
            }
            Toast.makeText(context, result.size(), Toast.LENGTH_LONG).show();
        }
    }
